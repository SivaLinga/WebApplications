package com.bcj.flightticket.modal;

public class BookingInfo {
	private int bookingId;
	private String itenaryID;
	private int bRefNumber;
	private String fare;
	private Ticket ticket;
	
	public int getBookingId() {
		return bookingId;
	}
	public void setBookingId(int bookingId) {
		this.bookingId = bookingId;
	}
	public String getItenaryID() {
		return itenaryID;
	}
	public void setItenaryID(String itenaryID) {
		this.itenaryID = itenaryID;
	}
	public int getbRefNumber() {
		return bRefNumber;
	}
	public void setbRefNumber(int bRefNumber) {
		this.bRefNumber = bRefNumber;
	}
	public String getFare() {
		return fare;
	}
	public void setFare(String fare) {
		this.fare = fare;
	}
	public Ticket getTicket() {
		return ticket;
	}
	public void setTicket(Ticket ticket) {
		this.ticket = ticket;
	}
}
